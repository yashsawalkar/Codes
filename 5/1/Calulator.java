class Calculator{

	public static void main(String[] args){
		
		//inline initialization
		int num1 = 20;
		int num2 = 10;
		int res = 0;

		//code
		
		//1. Addition
		res = num1 + num2;
		System.out.println(res);

		//2. Subtraction
		res = num1 - num2;
		System.out.println(res);

		//3. Multiplication
		res = num1 * num2;
		System.out.println(res);		

		//4. Division
		res = num1 / num2;
		System.out.println(res);		

		//5. Remainder operation
		num1 = 23;
		num2 = 10;
		res = num1 % num2;
		System.out.println(res);		

	}


}